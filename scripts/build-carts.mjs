function toBase32(data) {
  let encoded = [];
  let p = 0;
  let buf = 0;
  for (let byte of data) {
    buf = (buf << 8) | byte;
    p += 8;
    while (p >= 5) {
      p -= 5;
      let c = buf >> p;
      buf ^= c << p;
      if (c <= 25) encoded.push(97 + c);
      else encoded.push(24 + c);
    }
  }
  if (p > 0) {
    buf <<= 5 - p;
    if (buf <= 25) encoded.push(97 + buf);
    else encoded.push(24 + buf);
  }
  return String.fromCharCode(...encoded);
}

